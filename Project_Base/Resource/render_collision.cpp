//============================================================================
// 描画優先度指定コンストラクタ
//============================================================================
CRender_Collision::CRender_Collision(LAYER Priority) :
	CObject{ Priority },
	m_pRef{ nullptr },
	m_Col{ XCOL_INIT }
{
	// ワールド行列の初期化
	D3DXMatrixIdentity(&m_MtxWorld);
}

//============================================================================
// デストラクタ
//============================================================================
CRender_Collision::~CRender_Collision()
{

}

//============================================================================
// 初期設定
//============================================================================
HRESULT CRender_Collision::Init()
{
	return S_OK;
}

//============================================================================
// 終了処理
//============================================================================
void CRender_Collision::Uninit()
{

}

//============================================================================
// 更新処理
//============================================================================
void CRender_Collision::Update()
{
	// ワールド行列の設定
	SetMtxWorld();
}

//============================================================================
// 対象オブジェクトの設定
//============================================================================
void CRender_Collision::SetRefObj(CObject_X* pRef)
{
	m_pRef = pRef;
}

//============================================================================
// 
// privateメンバ
// 
//============================================================================

//============================================================================
// ワールド行列設定
//============================================================================
void CRender_Collision::SetMtxWorld()
{
	// 計算用行列
	D3DXMATRIX mtxRot, mtxTrans;

	// ワールド行列を初期化
	D3DXMatrixIdentity(&m_MtxWorld);

	// 平行移動行列作成
	D3DXMatrixTranslation(&mtxTrans,
		m_pRef->GetPos().x,
		m_pRef->GetPos().y,
		m_pRef->GetPos().z);

	// 平行移動行列との掛け算
	D3DXMatrixMultiply(&m_MtxWorld,
		&m_MtxWorld,
		&mtxTrans);
}