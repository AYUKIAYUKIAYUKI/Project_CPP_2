//============================================================================
// 初期設定
//============================================================================
HRESULT CPowerStone::Init()
{
	// アイテムクラスの初期設定
	if (FAILED(CItem::Init()))
	{
		return E_FAIL;
	}

	// 補間強度を設定
	SetCorrectCoef(0.1f);

	// モデルを設定
	BindModel(CX_Manager::TYPE::POWERSTONE);

	// 縮尺を設定
	SetScale(GetScale() * 1.0f);

	// 半径を設定
	SetRadius(5.0f);

	return S_OK;
}

