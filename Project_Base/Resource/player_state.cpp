CPlayer_State::CPlayer_State() :
	CState{},
	m_pPlayer{ nullptr },
	m_pNextState{ nullptr }
{

}

//============================================================================
// プレイヤー取得コンストラクタ
//============================================================================
CPlayer_State::CPlayer_State(player::CPlayer* pPlayer) :
	CState{},
	m_pPlayer{ pPlayer }
{
	if (pPlayer == nullptr)
	{
		assert(false && "プレイヤーステートがプレイヤーを取得失敗");
	}
}

