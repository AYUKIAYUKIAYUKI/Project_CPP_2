CLife::~CLife()
{

}

//============================================================================
// 初期設定
//============================================================================
HRESULT CLife::Init()
{
	// アイテムクラスの初期設定
	if (FAILED(CItem::Init()))
	{
		return E_FAIL;
	}

	// 補間強度を設定
	SetCorrectCoef(0.1f);

	// 半径を設定
	SetRadius(5.0f);

	return S_OK;
}

//============================================================================
// 終了処理
//============================================================================
void CLife::Uninit()
{
	// アイテムクラスの終了処理
	CItem::Uninit();
}

