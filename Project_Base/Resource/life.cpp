//============================================================================
// 初期設定
//============================================================================
HRESULT CLife::Init()
{
	// アイテムクラスの初期設定
	if (FAILED(CItem::Init()))
	{
		return E_FAIL;
	}

	// 補間強度を設定
	SetCorrectCoef(0.1f);

	// モデルを設定
	BindModel(CX_Manager::TYPE::LIFE);

	// 縮尺を設定
	SetScale(GetScale() * 0.75f);

	// 半径を設定
	SetRadius(5.0f);

	return S_OK;
}

