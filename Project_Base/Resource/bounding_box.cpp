//============================================================================
// コンストラクタ
//============================================================================
CBounding_Box::CBounding_Box() :
	CBounding_Volume{},
	m_Size{ VEC3_INIT },
	m_pRenderBox{ nullptr }
{
	
}

//============================================================================
// ボックス表示コンストラクタ
//============================================================================
CBounding_Box::CBounding_Box(CObject_X* pObj) :
	CBounding_Volume{},
	m_Size{ VEC3_INIT },
	m_pRenderBox{ CRender_Box::Create(pObj) }
{

}

//============================================================================
// デストラクタ
//============================================================================
CBounding_Box::~CBounding_Box()
{
	// ボックス表示の破棄
	if (m_pRenderBox != nullptr)
	{
		// 破棄予約
		m_pRenderBox->SetRelease();

		// ポインタを初期化
		m_pRenderBox = nullptr;
	}
}

//============================================================================
// サイズを取得
//============================================================================
const D3DXVECTOR3& CBounding_Box::GetSize() const
{
	return m_Size;
}

//============================================================================
// サイズを設定
//============================================================================
void CBounding_Box::SetSize(D3DXVECTOR3 Size)
{
	m_Size = Size;
}