//============================================================================
// 取得時の効果
//============================================================================
void CWings::GainEffect()
{
	// プレイヤーを検索
	CObject* pFindPlayer = CObject::FindSpecificObject(CObject::TYPE::PLAYER);

	// プレイヤーを発見出来たら
	if (pFindPlayer)
	{
		// プレイヤークラスにダウンキャスト
		CPlayer* pPlayer = utility::DownCast<CPlayer, CObject>(pFindPlayer);

		// 浮遊を有効化
		pPlayer->EnableWings();

		// 取得時のテキストを表示
		DispGetText("Data\\JSON\\TEXTMESH\\text_getwings.json");
	}
}