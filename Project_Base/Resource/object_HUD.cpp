//============================================================================
CObject_HUD::~CObject_HUD()
{

}

//============================================================================
// 初期設定
//============================================================================
HRESULT CObject_HUD::Init()
{
	// 2Dオブジェクトの初期設定
	if (FAILED(CObject_2D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//============================================================================
// 終了処理
//============================================================================
void CObject_HUD::Uninit()
{
	// 2Dオブジェクトの終了処理
	CObject_2D::Uninit();
}

//============================================================================
// 更新処理
//============================================================================
void CObject_HUD::Update()
{
	// 目標値への補正
	AdjustToTarget();

	// 2Dオブジェクトの更新処理
	CObject_2D::Update();
}

//============================================================================
// 描画処理
//============================================================================
void CObject_HUD::Draw()
{
	// 2Dオブジェクトの描画処理
	CObject_2D::Draw();
}

