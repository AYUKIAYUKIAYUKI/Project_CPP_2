CObject_HUD::CObject_HUD(int nPriority) :
	CObject_2D{ nPriority },
	m_PosTarget{ VEC3_INIT },
	m_RotTarget{ VEC3_INIT },
	m_SizeTarget{ VEC3_INIT },
	m_ColTarget{ XCOL_INIT }
{

}

