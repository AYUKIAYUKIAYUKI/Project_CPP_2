// 終了処理
//============================================================================
void CPlayer::Uninit()
{
	// 基底クラスの終了処理
	CObject_X::Uninit();
}

//============================================================================
// 更新処理
//============================================================================
void CPlayer::Update()
{
	// 操作
	Control();

	// 基底クラスの更新処理
	CObject_X::Update();

#ifdef _DEBUG
	CRenderer::GetInstance()->SetDebugString("プレイヤー座標 : " + std::to_string(GetPos().x) + " :  " + std::to_string(GetPos().y) + " : " + std::to_string(GetPos().z));
#endif // _DEBUG
}

